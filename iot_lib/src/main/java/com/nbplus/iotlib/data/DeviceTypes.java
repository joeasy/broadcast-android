// device list option
public enum DeviceTypes {
    ALL ("All"),
    BT ("BT"),
    IR ("IR"),
    ZWAVE ("ZW");

    private final String name;

    private DeviceTypes(String s) {
        name = s;
    }

    public boolean equalsName(String otherName) {
        return (otherName == null) ? false : name.equals(otherName);
    }

    public String toString() {
        return this.name;
    }
}

